-- Script to create the DEMO_1 database

CREATE DATABASE [DEMO_1];
GO

USE [DEMO_1];

CREATE TABLE PERSON 
(Per_ID CHAR (10) NOT NULL, 
Cust_FName VARCHAR (25) NOT NULL, 
Cust_LName VARCHAR (25) NOT NULL, 
PRIMARY KEY (Per_ID) ); 

CREATE TABLE SS_WEBSITE_USER 
(User_ID VARCHAR (25) NOT NULL, 
Per_ID CHAR (10) NOT NULL, 
Email_Addr VARCHAR (50) NOT NULL, 
Password VARCHAR (25) NOT NULL, 
PRIMARY KEY (User_ID), 
FOREIGN KEY (Per_ID) REFERENCES PERSON (Per_ID) ); 

CREATE TABLE ACCOUNT 
(Acct_ID CHAR (10) NOT NULL, 
Per_ID CHAR (10) NOT NULL, 
House_Num VARCHAR (25) NOT NULL, 
Street_Nm VARCHAR (50) NOT NULL, 
City VARCHAR (50) NOT NULL, 
State CHAR (2) NOT NULL, 
ZIP INT NOT NULL, 
Bill_Type VARCHAR (10) NOT NULL, 
PRIMARY KEY (Acct_ID), 
FOREIGN KEY (Per_ID) REFERENCES PERSON (Per_ID) ); 

CREATE TABLE BILL 
(Bill_ID CHAR (12) NOT NULL, 
Acct_ID CHAR (10) NOT NULL, 
Current_Balance NUMERIC (7,2) NOT NULL, 
Past_Due_Amt NUMERIC (7,2) NOT NULL, 
Current_Amt_Due NUMERIC (7,2) NOT NULL, 
Due_Date DATE NOT NULL, 
PRIMARY KEY (Bill_ID), 
FOREIGN KEY (Acct_ID) REFERENCES ACCOUNT (Acct_ID) ); 

CREATE TABLE PAYMENT 
(Pay_ID CHAR (15) NOT NULL, 
Bill_ID CHAR (12) NOT NULL, 
Pay_Amt NUMERIC (7,2) NOT NULL, 
Pay_Date DATE NOT NULL, 
Pay_Method VARCHAR (15) NOT NULL, 
CC_Exp_Date DATE NULL, 
PRIMARY KEY (Pay_ID), 
FOREIGN KEY (Bill_ID) REFERENCES BILL (Bill_ID) ); 

CREATE TABLE APPOINTMENT 
(Appt_ID CHAR (10) NOT NULL, 
Acct_ID CHAR (10) NOT NULL, 
Appt_Date DATE NOT NULL, 
Appt_Type VARCHAR (25) NOT NULL, 
Spec_Instr VARCHAR (100) NULL, 
Appt_Status VARCHAR (25) NOT NULL, 
PRIMARY KEY (Appt_ID), 
FOREIGN KEY (Acct_ID) REFERENCES ACCOUNT (Acct_ID) );

